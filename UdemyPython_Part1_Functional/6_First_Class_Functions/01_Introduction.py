
## All functions in pythons are First Class functions. But before understanding First Class Functions it is necessary to understand First Class Objects
## An object is first class if it has following properties
## 1. It can be passed to a function as an argument
## 2. Can be returned from a function
## 3. Can be assigned to a variable
## 4. Can be stored in a data structure(such as List, Tuple etc)

## High Order Functions - Functions that can take a function as an argument and or can return a function